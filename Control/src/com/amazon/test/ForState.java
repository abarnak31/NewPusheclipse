package com.amazon.test;

public class ForState {

	public static void main(String[] args) {
		System.out.println("1 to 5 order");
		int no = 1;
		while ( no <= 5 ) {
			System.out.println(no);
			no++;
		}
		System.out.println("1 to 5 in reverse order");
		int n = 5;
		while ( n >= 1 ) {
			System.out.println(n);
			n--;
		}

	}
}
